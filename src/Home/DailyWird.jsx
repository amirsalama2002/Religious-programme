function Quiz() {
  const [index, setIndex] = useState(0);
  const [selected, setSelected] = useState(null);
  const [showResult, setShowResult] = useState(false);

  const current = questions[index];

  const handleAnswer = (i) => {
    setSelected(i);
    setShowResult(true);
  };

  const nextQuestion = () => {
    setSelected(null);
    setShowResult(false);
    setIndex((prev) => (prev + 1) % questions.length);
  };

  return (
    <div className="max-w-6xl mx-auto p-6 mt-10 text-center bg-white dark:bg-gray-900 shadow-xl rounded-xl">
      <h1 className="text-2xl font-bold text-blue-600 dark:text-yellow-400 mb-6">
        ❓ سؤال اليوم الديني
      </h1>

      <h2 className="text-xl font-semibold mb-4 dark:text-white">{current.question}</h2>

      <div className="grid grid-cols-1 sm:grid-cols-2 gap-4 mb-6">
        {current.options.map((option, i) => (
          <button
            key={i}
            disabled={showResult}
            onClick={() => handleAnswer(i)}
            className={`px-4 py-2 rounded-lg border transition-all duration-200 text-lg font-medium
              ${
                showResult
                  ? i === current.correct
                    ? "bg-green-500 text-white border-green-600"
                    : i === selected
                    ? "bg-red-500 text-white border-red-600"
                    : "bg-gray-100 dark:bg-gray-700 dark:text-white"
                  : "bg-gray-100 hover:bg-blue-100 dark:bg-gray-800 dark:hover:bg-gray-700 dark:text-white"
              }`}
          >
            {option}
          </button>
        ))}
      </div>

      {showResult && (
        <div className="mt-4 bg-blue-50 dark:bg-gray-800 text-blue-800 dark:text-white p-4 rounded-md">
          {selected === current.correct ? (
            <p className="font-semibold text-green-600 dark:text-green-400">✔️ إجابة صحيحة!</p>
          ) : (
            <p className="font-semibold text-red-600 dark:text-red-400">
              ❌ إجابة خاطئة. الصحيح: {current.options[current.correct]}
            </p>
          )}
          <p className="text-sm mt-2">{current.explanation}</p>

          <button
            onClick={nextQuestion}
            className="mt-4 bg-blue-600 hover:bg-blue-700 text-white px-5 py-2 rounded"
          >
            سؤال جديد 🔁
          </button>
        </div>
      )}
    </div>
  );
}

export default Quiz;
